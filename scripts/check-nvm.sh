#!/bin/bash
nvm --version > /dev/null 2>&1
if [ $? -eq 0 ]
then
   nvm use 12.18.0 --lts
else
   echo 'nvm 未安装，使用环境默认的 node 版本进行任务构建！！'
fi

# nvm use 12.18.0 --lts 可以切换到 12.18.3
# 可以将 /dev/null 看作"黑洞". 它非常等价于一个只写文件. 所有写入它的内容都会永远丢失. 而尝试从它那儿读取内容则什么也读不到. 
# > 代表重定向到哪里，例如：echo "123" > /home/123.txt 
# 1 表示stdout标准输出，系统默认值是1，所以">/dev/null"等同于"1>/dev/null" 
# 2 表示stderr标准错误 
# & 表示等同于的意思，2>&1，表示2的输出重定向等同于1

# $? :获取函数的返回值或者上一个命令的退出状态 。如果成功就是0，失败为1
# 常用if [ $? -eq 0 ] 来判断一个命令有没有执行成功